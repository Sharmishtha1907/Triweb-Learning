const square= function (a){
     let sqr=a*a;
     console.log("SQUARE OF ",a," IS : ",sqr);
}

square(3);
square(4);

const cube= function (a){
    let cubee=a*a*a;
    console.log("CUBE OF ",a," IS : ",cubee);
}

cube(2);
cube(3);

/* OUTPUT 
SQUARE OF  3  IS :  9
SQUARE OF  4  IS :  16
CUBE OF  2  IS :  8
CUBE OF  3  IS :  27
*/